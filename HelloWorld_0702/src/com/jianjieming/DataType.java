package com.jianjieming;

public class DataType {
    public static void main(String[] args) {
        byte b = (byte)300;
        System.out.println(b);

        char a = 'A';
        System.out.println(a);

        char s = (char)120;
        System.out.println(s);

        int c = a;
        System.out.println(c);

        char d = (char)58;
        System.out.println(d);
    }
}
